// String type variable***
var name1 = 'tanvir';
console.log(name1);
// ans :-  tanvir

var name2 = "hasan"; //it can be single quotation or double quotation
console.log(name2);
// ans :-  hasan


// Number type variable***
var boyos = 20;
console.log(boyos)
// ans :- 20


// Boolean type variable***
var isGoing = true;
console.log(isGoing)
// ans :- true

var isComing = false;
console.log(isComing);
// ans :- false



